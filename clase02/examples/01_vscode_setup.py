# Este archivo no contiene código ejecutable, pero explica cómo configurar Visual Studio Code para Python.

# 1. Descarga e instala Visual Studio Code desde: https://code.visualstudio.com/
# 2. Instala la extensión de Python:
#    - Abre VS Code.
#    - Ve a "Extensions" (icono de cuadrados).
#    - Busca "Python" y selecciona la extensión oficial de Microsoft.
#    - Instala la extensión.
#    - Busca spanish
#    - Instala Spanish Language Pack for Visual Studio Code
#    - Reiniciar VSCode   
# 3. Configura el entorno de Python:
#    - Abre una carpeta de proyecto.
#    - Crea un archivo `hola_mundo.py` (explicado en otro archivo).
#    - Ejecuta el archivo con "Run" o seleccionando el ícono de "play" en la parte superior derecha.
