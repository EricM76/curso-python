# Python tiene varios tipos de datos simples, como:
# int (números enteros), float (números decimales), str (cadenas de texto), y bool (valores lógicos)

# Ejemplos de tipos de datos:
entero = 10  # Un número entero
decimal = 4.5  # Un número flotante (decimal)
cadena = "Hola"  # Una cadena de texto (string)
booleano = True  # Un valor booleano (verdadero o falso)

# Imprimir tipos de datos
print("El tipo de 'entero' es", type(entero))  # Muestra <class 'int'>
print("El tipo de 'decimal' es", type(decimal))  # Muestra <class 'float'>
print("El tipo de 'cadena' es", type(cadena))  # Muestra <class 'str'>
print("El tipo de 'booleano' es", type(booleano))  # Muestra <class 'bool'>
