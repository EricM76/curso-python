# Comentarios en Python:
# Los comentarios se inician con el símbolo # y no son ejecutados por Python.
'''
Para comentar un bloque de codigo o texto se usan triple comillas.
'''

# Indentación:
# La indentación es importante en Python. Cada bloque de código debe estar correctamente indentado.
if True:  # Si la condición es verdadera
    print("Este bloque está indentado correctamente.")  # Este código se ejecuta

# Variables:
# No es necesario declarar el tipo de las variables en Python. Se asignan automáticamente según el valor.
x = 5  # Una variable entera
y = 3.14  # Una variable de tipo flotante
nombre = "Juan"  # Una variable de tipo cadena (string)

# Para imprimir múltiples valores, se usa la coma:
print("El valor de x es", x, "y el de y es", y)
