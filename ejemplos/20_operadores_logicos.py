""" 
==================
OPERADORES LÓGICOS
==================

Se utilizan los operadores lógicos para tomar decisiones basada en múltiples condiciones. 
Los operadores lógicos utilizados en Python son  and, or y not.
"""
a = True
b = False

# OPERADOR and
# Devuelve True si ambos operandos son True

print(a and b) # False

# OPERADOR or
# Devuelve True si alguno de los operandos es True

print(a or b) # True