""" 
=======================
OPERADORES RELACIONALES
=======================

Los operadores relacionales en Python, que también se conocen como operadores de comparación, 
sirven para algo clave: comparar valores. El resultado de esta comparación siempre es un valor booleano, 
es decir, True (verdadero) o False (falso), dependiendo de si la condición que estás evaluando es cierta 
o no. Esto es súper importante, porque estos operadores son los que te permiten tomar decisiones en tus 
programas. 
"""

# OPERADOR ==
# Igual a: compara si dos valores son iguales.
print(7 == 7) # True
print(9 == 10) # False

# OPERADOR !=
# Diferente de: compara si dos valores no son iguales.
print(7 != 7) # False
print(9 != 10) # True

# OPERADOR <
# Menor que: verifica si el valor de la izquierda es menor que el de la derecha.
print(7 < 7) # False
print(9 < 10) # True

# OPERADOR >
# Mayor que: verifica si el valor de la izquierda es mayor que el de la derecha.
print(8 > 6) # True
print(5 > 5) # False

# OPERADOR <=
# Menor o igual que: verifica si el valor de la izquierda es menor o igual al de la derecha.
print(8 <= 6) # False
print(5 <= 5) # True

# OPERADOR >=
# Mayor o igual que: verifica si el valor de la izquierda es mayor o igual al de la derecha.
print(8 >= 8) # True
print(4 >= 5) # False